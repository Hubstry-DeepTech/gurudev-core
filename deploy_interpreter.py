def write_file(relpath, b64, label):
    full = os.path.join(BASE, relpath)
    os.makedirs(os.path.dirname(full), exist_ok=True)
    data = base64.b64decode(b64)
    with open(full, "wb") as f:
        f.write(data)
    print(f"  [{label}] {relpath} ({len(data)} bytes)")

